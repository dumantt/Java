package day01_programlamayaGiris;

public class C02Variables {
    public static void main(String[] args) {
        int sayi = 20;
        System.out.println(sayi);
        // " işaretinin içindekini aynen yazdırır işaret yoksa bunu variable olarak kabul eder
        sayi = 30;
        System.out.println(sayi);
        sayi = sayi + 20;
        System.out.println(sayi);

    }
}
